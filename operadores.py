#OPERADORES

#operaciones básicas
print(3+4)
print(3-4)
print(3*4)
print(3/4)

#operadores siguientes
print(10%3) # 10/3 es 3, entonces será 10- 9 = 1
print(10%2) # 10/2 es 5, entonces será 10- 10 = 0

#También se pueden hacer en strings, pero solo sumar
print("hola" + "mundo")
#no puedes mezclar tipos de dato

print("hola " + str(2026)) #Transformamos a string el número para que podamos hacerlo

#OPERADORES COMPARATIVOS

print(3>4) #3 es mayor que 4? false
print(3<4) #3 es menor que 4? true
print(3<=4) #3 es menor o igual que 4? verdadero
print(3>=4) #3 es mayor o igual que 4? verdadero
print(3==4) #3 es igual que 4? false
print(3!=4) # 3 es distinto que 4?


